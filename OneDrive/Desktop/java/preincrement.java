import java.util.*;
public class preincrement {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        int b = sc.nextInt();
        int c = ++a;
        int d = --b;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);

        
    }
    
}
